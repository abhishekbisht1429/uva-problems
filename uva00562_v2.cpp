typedef _p<int, int> _ii;

int m;
_vec<int> coins;
_vec<_vec<int>> dp;
int total;

/* top down approach */
int findOptSol(int s=total/2, int i=0) {
    if(i==m)
        return 0;

    if(dp[s][i] == -1) {
        int v1, v2 = 0;
        v1 = findOptSol(s, i+1);
        if(s - coins[i] >= 0)
            v2 = coins[i] + findOptSol(s-coins[i], i+1);
        
        dp[s][i] = max(v1, v2);
    }

    return dp[s][i];
}

